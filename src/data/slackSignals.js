// Helper to look up a deal by company name extracted from deal_id
export function getSlackSignal(dealName) {
  if (!dealName) return null;

  // Extract company name from deal_id format: "Company - Series X" or "Series X - Company"
  const parts = dealName.split(/\s*[-\u2013\u2014]\s*/);
  for (const part of parts) {
    const clean = part.trim().toLowerCase()
      .replace(/^(series [a-z+]|seed|pre-seed|venture round|venture|grant|private equity|corporate|m&a|safe|secondary)\s*(round)?$/i, '')
      .trim();
    if (clean && clean.length > 1) {
      // Try exact match first
      if (SLACK_SIGNALS[clean]) return SLACK_SIGNALS[clean];
      // Try partial matches (both directions)
      for (const key of Object.keys(SLACK_SIGNALS)) {
        if (key.includes(clean) || clean.includes(key)) return SLACK_SIGNALS[key];
      }
    }
  }
  return null;
}
