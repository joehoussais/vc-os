// Calculate stats from data
export function calculateCoverageStats(deals, filters = {}) {
  let filteredDeals = deals;

  if (filters.country && filters.country !== 'all') {
    filteredDeals = filteredDeals.filter(d => d.filterRegion === filters.country);
  }
  if (filters.stage && filters.stage !== 'all') {
    filteredDeals = filteredDeals.filter(d => d.stage === filters.stage);
  }

  const inScopeDeals = filteredDeals.filter(d => d.inScope);
  const seenDeals = inScopeDeals.filter(d => d.seen);
  const coverage = inScopeDeals.length > 0
    ? Math.round((seenDeals.length / inScopeDeals.length) * 100)
    : 0;

  return {
    total: filteredDeals.length,
    inScope: inScopeDeals.length,
    seen: seenDeals.length,
    coverage,
  };
}

// Calculate coverage by country
export function calculateCoverageByCountry(deals) {
  const regions = ['France', 'Germany', 'Nordics', 'Southern Europe', 'Eastern Europe', 'Other'];
  const result = {};

  for (const region of regions) {
    const regionDeals = deals.filter(d => d.filterRegion === region);
    const inScope = regionDeals.filter(d => d.inScope);
    const seen = inScope.filter(d => d.seen);
    result[region] = {
      total: regionDeals.length,
      inScope: inScope.length,
      seen: seen.length,
      coverage: inScope.length > 0 ? Math.round((seen.length / inScope.length) * 100) : 0,
    };
  }

  return result;
}

